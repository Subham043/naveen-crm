<?php

namespace App\Features\Order\Requests;

use App\Features\Order\Enums\OrderStatus;
use App\Http\Enums\Guards;
use App\Http\Requests\InputRequest;
use Illuminate\Support\Facades\Auth;
use Illuminate\Validation\Rules\Enum;
use Illuminate\Validation\Rule;

class OrderApprovalRequests extends InputRequest
{
    /**
     * Determine if the user is authorized to make this request.
     *
     * @return bool
     */
    public function authorize(): bool
    {
        return Auth::guard(Guards::API->value())->check();
    }

    /**
     * Get the validation rules that apply to the request.
     *
     * @return array
     */
    public function rules()
    {
        return [
            'order_status' => ['required', 'numeric', new Enum(OrderStatus::class), Rule::notIn([OrderStatus::Pending->value])],
        ];
    }

}
